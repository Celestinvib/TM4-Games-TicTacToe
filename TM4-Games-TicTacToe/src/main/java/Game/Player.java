package Game;

public class Player {
	
	/** Attributes */
	private String token;
	
	private String name;
	
	private String type;


	/**Constructor */
	/**
	 * @param token
	 * @param name
	 * @param type
	 */
	public Player(String token, String name, String type) {
		this.token = token;
		this.name = name;
		this.type = type;
	}
	
	
	


}
